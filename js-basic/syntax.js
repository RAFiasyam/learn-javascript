// JavaScript Values
/**
 * JavaScript memiliki 2 jenis nilai (values):
 * 1. Fixed Values -> disebut Literal Values
 * 2. Variable Values -> disebut Variables
 */

// Literal Values
const number = 42;      // 42 adalah literal value 
const text = "Hello";   // "Hello" adalah literal value
// singkat nya adalah literal value itu gk bisa diubah sesuai namanya itu fixed value

// Variables
let age = 25;           // age adalah variable, 25 adalah literal
age = 30;               // nilai age berubah jadi 30

