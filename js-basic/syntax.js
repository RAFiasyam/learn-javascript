// JavaScript and Camel Case
/** 
 * Secara historis, pemrogram telah menggunakan berbagai cara untuk bergabung dengan banyak kata menjadi satu nama variabel:
 *  `Underscore:`
 *  first_name, last_name, master_card, inter_city.
 * 
 *  `Upper Camel Case (pascal case):`
 *  FirstName, LastName, MasterCard, InterCity.
 * 
 *  `Lower Camel Case:`
 *  firstName, lastName, masterCard, interCity. //JavaScript programmers tend to use camel case that starts with a lowercase letter
 */